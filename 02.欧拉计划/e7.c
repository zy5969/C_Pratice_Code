//1001 * 1001的螺旋矩阵，对角线数字和，
//圈数1、3、5、7递增，右上角为边长平方
//5 * 5   101
#include <stdio.h>

int main() {
    int sum = 1;
    for(int i = 3; i <= 1001; i += 2){
        sum += i * i * 4 - 6 * i + 6;
    }
    printf("%d\n", sum);
    return 0;
}


